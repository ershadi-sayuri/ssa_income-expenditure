    private void save_userButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_userButtonActionPerformed
        try {
            if (!add_userNameTextFiled.getText().isEmpty() && !add_passwordField.getText().isEmpty() && !add_confirmPasswordField.getText().isEmpty()) {
                if (add_passwordField.getText().equals(add_confirmPasswordField.getText())) {
                    String user_password = add_passwordField.getText();
                    String encrypted_password = dataProtector.encrypt(user_password, "a", 3);

                    int result = userController.addUser(add_userIdLabel.getText(), add_userNameTextFiled.getText(), encrypted_password);

                    if (result == 1) {
                        JOptionPane.showMessageDialog(rootPane, "User saved successfully.");

                        add_userIdLabel.setText(idGenerator.generateId("uu"));
                        add_userNameTextFiled.setText(null);
                        add_passwordField.setText(null);
                        add_confirmPasswordField.setText(null);

                        loadComboForUser();
                        loadTableForUser();
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "User saving failed. Please retry saving with corrrect details.");
                    }

                } else {
                    JOptionPane.showMessageDialog(rootPane, "Passwords do not match. Please enter them again");
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "None of the fields cannot be null. Please recheck before you save user.");
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(ManageIncomeAndExpenditureReason.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(ManageUser.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_save_userButtonActionPerformed

