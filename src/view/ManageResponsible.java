    private void loadComboForResponsible() throws ClassNotFoundException, SQLException {
        switch (searchDelete_responsibleFirstLabel.getText()) {
            case "Responsible id :":
                searchDelete_responsibleComboBox.removeAllItems();
                comboBoxFunctions.loadCombo(searchDelete_responsibleComboBox, "responsible", "responsible_id");
                comboSearch1.setSearchableCombo(searchDelete_responsibleComboBox, true, "No such responsible available");
                break;
            case "Responsible name :":
                searchDelete_responsibleComboBox.removeAllItems();
                comboBoxFunctions.loadCombo(searchDelete_responsibleComboBox, "responsible", "responsible_name");
                comboSearch1.setSearchableCombo(searchDelete_responsibleComboBox, true, "No such responsible available");
                break;
        }

        update_responsibleIdComboBox.removeAllItems();
        comboBoxFunctions.loadCombo(update_responsibleIdComboBox, "responsible", "responsible_id");
        comboSearch2.setSearchableCombo(update_responsibleIdComboBox, true, "No such iresponsible available");
    }

    private void loadTableForResponsible() throws ClassNotFoundException, SQLException {
        dtmForResponsible.setRowCount(0);
        ArrayList<Object[]> arrayList = responsibleController.searchAllResponsible();

        for (int i = 0; i < arrayList.size(); i++) {
            dtmForResponsible.addRow(arrayList.get(i));
        }
    }

