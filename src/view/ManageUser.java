    private void loadComboForUser() throws ClassNotFoundException, SQLException {
        if (delete_userIdRadioButton.isSelected()) {
            delete_userComboBox.removeAllItems();
            comboBoxFunctions.loadCombo(delete_userComboBox, "user", "user_id");
            comboSearch1.setSearchableCombo(delete_userComboBox, true, "No such user available");
        } else if (delete_userNameRadioButton.isSelected()) {
            delete_userComboBox.removeAllItems();
            comboBoxFunctions.loadCombo(delete_userComboBox, "user", "user_name");
            comboSearch2.setSearchableCombo(delete_userComboBox, true, "No such user available");
        }

        if (update_userIdRadioButton.isSelected()) {
            update_userComboBox.removeAllItems();
            comboBoxFunctions.loadCombo(update_userComboBox, "user", "user_id");
            comboSearch1.setSearchableCombo(update_userComboBox, true, "No such user available");
        } else if (update_userNameRadioButton.isSelected()) {
            update_userComboBox.removeAllItems();
            comboBoxFunctions.loadCombo(update_userComboBox, "user", "user_name");
            comboSearch2.setSearchableCombo(update_userComboBox, true, "No such user available");
        }
    }

    private void loadTableForUser() throws ClassNotFoundException, SQLException {
        dtmForUser.setRowCount(0);
        ArrayList<Object[]> arrayList = userController.searchAllUser();

        for (int i = 0; i < arrayList.size(); i++) {
            dtmForUser.addRow(arrayList.get(i));
        }
    }

