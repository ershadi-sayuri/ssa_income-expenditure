    private void save_responsibleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_responsibleButtonActionPerformed
        try {
            if (!add_responsibleNameText.getText().isEmpty()) {
                int result = responsibleController.addResponsible(add_responsibleIdLabel.getText(), add_responsibleNameText.getText());

                if (result == 1) {
                    JOptionPane.showMessageDialog(rootPane, "Responsible saved successfully.");

                    add_responsibleIdLabel.setText(idGenerator.generateId("rr"));
                    add_responsibleNameText.setText(null);

                    loadComboForResponsible();
                    loadTableForResponsible();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Responsible saving failed. Please retry saving with corrrect details.");
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "None of the fields cannot be null. Please recheck before you save respomsible.");
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(ManageResponsible.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_save_responsibleButtonActionPerformed

